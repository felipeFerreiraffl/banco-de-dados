# Funções de grupo

-- Contar os elementos de uma tabela
SELECT COUNT(*) FROM cliente;

-- Média dos elementos de uma tabela
SELECT AVG(valor) FROM pagamento;

-- Maior dos elementos de uma tabela
SELECT MAX(valor) FROM pagamento;

-- Menor dos elementos de uma tabela
SELECT MIN(valor) FROM pagamento;

-- Soma dos elementos de uma tabela
SELECT SUM(valor) FROM pagamento;